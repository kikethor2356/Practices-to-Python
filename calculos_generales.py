def sumar(op1, op2):
	print("El resulatado de la suma es:", op1 + op2)

def restar(op1,op2):
	print("El resulatado de la resta es :", op1-op2)

def multiplicar(op1,op2):
	print("El resulatado de la multiplicacion es:", op1*op2)

def dividir(op1,op2):
	print("El resulatado de la division es:",op1/op2)	

def potencia(base,exponente):
	print("El resulatado de la suma es:", base**exponente)	

